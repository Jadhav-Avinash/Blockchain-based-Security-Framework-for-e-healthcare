    void setaid()
    {
        try
        {
             Connection con=new dbconnect().connect();
             
             PreparedStatement pst=con.prepareStatement("select aid from appointment order by aid desc limit 1");
             ResultSet rs=pst.executeQuery();
             
             if (rs.next())
             {
                 int aid=rs.getInt(1);
                 aid++;
                 jTextField1.setText(""+aid);
             }
             else
             {
                 jTextField1.setText("1");
             }
        }
        catch(Exception e)
        {
            System.out.println(e);
            e.printStackTrace();
        }
    }
    
    void getdoctors()
    {
        try
        {
            Connection con=new dbconnect().connect();
            
            PreparedStatement pst=con.prepareStatement("select did,dname,speciality from doctor");
            ResultSet rs=pst.executeQuery();
            while (rs.next())
            {
                did.add(rs.getString("did"));
                dname.add(rs.getString("dname"));
                spec.add(rs.getString("speciality"));
            }
            
        }
        catch(Exception e)
        {
            System.out.println(e);
            e.printStackTrace();
        }
    }

