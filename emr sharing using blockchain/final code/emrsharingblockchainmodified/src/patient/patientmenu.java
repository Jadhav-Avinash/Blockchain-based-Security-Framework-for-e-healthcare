    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        // TODO add your handling code here:
        try
        {
            if (jComboBox2.getSelectedIndex()>0)
            {
                Socket soc=new Socket(ips.blockchainip,ips.blockchainport);
                ObjectOutputStream oos=new ObjectOutputStream(soc.getOutputStream());
                ObjectInputStream oin=new ObjectInputStream(soc.getInputStream());
           
                oos.writeObject("GETMYREPORT");
           
                oos.writeObject(Integer.parseInt(jComboBox2.getSelectedItem().toString().trim()));
                                                   
                String reply=(String)oin.readObject();
                
                if (reply.equals("FOUND"))
                {
                    String problem=(String)oin.readObject();
                    String test=(String)oin.readObject();
                    String treport=(String)oin.readObject();
                    String report=(String)oin.readObject();
                    
                    jTextField2.setText(problem);
                    jTextArea1.setText(test);
                    if (treport.equals("NOTYET"))
                    jTextArea2.setText(treport);
                    else
                    jTextArea2.setText(new DES().decrypt(treport,"12345678"));
                    jTextArea3.setText(report);
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"No reports found!");
                }
                
                oos.close();
                oin.close();
                soc.close();
                    
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jComboBox2ActionPerformed

