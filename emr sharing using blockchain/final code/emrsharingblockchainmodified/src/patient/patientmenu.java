    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try
        {
            Connection con=new dbconnect().connect();
            PreparedStatement pst=con.prepareStatement("select * from appointment where pid=?");
            pst.setInt(1,pid);
            ResultSet rs=pst.executeQuery();
            
            DefaultTableModel dft=(DefaultTableModel)jTable1.getModel();
            
            while (rs.next())
            {
                Vector v=new Vector();
                v.add(rs.getInt(1));
                int did=rs.getInt(2);
                PreparedStatement pst2=con.prepareStatement("select dname from doctor where did=?");
                pst2.setInt(1,did);
                ResultSet rs2=pst2.executeQuery();
                rs2.next();
                
                v.add(rs2.getString(1));
                v.add(rs.getString(4));
                v.add(rs.getString(5));
                
                dft.addRow(v);
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

